def add(a, b):
    """두 수의 덧셈 결과를 반환합니다."""
    return a + b

def subtract(a, b):
    """두 수의 뺄셈 결과를 반환합니다."""
    return a - b

def multiply(a, b):
    """두 수의 곱셈 결과를 반환합니다."""
    return a * b

# 0으로 나누는 경우에 대한 예외 처리를 포함하지 않은 단순 나눗셈
def divide(a, b):
    """두 수의 나눗셈 결과를 반환합니다."""
    return a / b